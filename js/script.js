backOnTopBtn = document.getElementById("backOnTopBtn");
window.onscroll = function(){scrollFunction()};
				
function scrollFunction(){
	if(document.body.scrollTop > 20 || document.documentElement.scrollTop > 20) backOnTopBtn.style.display = "block";
	else backOnTopBtn.style.display = "none";
}
				
function backOnTop(){
	document.body.scrollTop = 0;
	document.documentElement.scrollTop = 0;
}

//Animation on left side menu
$(document).ready(function () {
    $(document).on("scroll", onScroll);
    
    //smoothscroll
    $('a[href^="#"]').on('click', function (e) {
        e.preventDefault();
        $(document).off("scroll");
        
        $('a').each(function () {
            $(this).removeClass('active');
        })
        $(this).addClass('active');
      
        var target = this.hash,
            menu = target;
        $target = $(target);
        $('html, body').stop().animate({
            'scrollTop': $target.offset().top+2
        }, 500, 'swing', function () {
            window.location.hash = target;
            $(document).on("scroll", onScroll);
        });
    });
});

function onScroll(event){
    var scrollPos = $(document).scrollTop();
    $('#nav a').each(function () {
        var currLink = $(this);
        var refElement = $(currLink.attr("href"));
        if (refElement.position().top <= scrollPos && refElement.position().top + refElement.height() > scrollPos) {
            $('#nav ul li a').removeClass("active");
            currLink.addClass("active");
        }
        else{
            currLink.removeClass("active");
        }
    });
}